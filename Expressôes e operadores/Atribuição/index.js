// Recebe
var i = 1
console.log(i)

// adicionar e atribuir
// i = i + 2
i += 2
console.log(i)

// subitrair e atribuir
// i = i - 2
i -= 2
console.log(i)

// multiplicar e atribuir
// i = i * 2
i *= 2
console.log(i)

// dividir e atribuir
// i = i / 2
i /= 2
console.log(i)


// modulos e atribuir
// i = i % 2
i %= 2
console.log(i)


// exponenciação e atribuir
// i = i ** 2
i **= 2
console.log(i)