/*
    opedadores de comparação
        == igual
        === estritamente igula
        != diferente
        !== estritamente diferente
*/
let um = 1
let dois = 2

console.log(um == dois) // true
console.log(um === "dois") // false
console.log(um =! dois) // false

/*
    opedadores de comparação
        > maior que
        => igual ou maior que
        < menor que
        =< igual ou menor que
        
*/
console.log(um > dois) // false
console.log(um >= dois) // true
console.log(um < dois) // false
console.log(um <= dois) // true
