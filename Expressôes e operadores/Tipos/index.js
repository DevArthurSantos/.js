/* 

Expressões
        * Unarios (unary) // recebe apenas um valor
        * Binarios (binary) // recebe dois valores
        * Ternarios (Ternary) // recebe tres valores
*/

// Expressões
// Unarios (unary)
//     1
let number1 = 1; // Unario
console.log(number1)

// Binarios (binary)
let number2 = 2; // Unario

//            1         2
let soma = number1 + number2 // Binario
console.log(soma)

// Ternarios (Ternary)
//            1       2       3
console.log(true ? "alo" : "nada") // Ternario

